CREATE VIEW MEDIA_TECNICOS AS SELECT AVG(TEC) FROM (SELECT I.NO_IES,T.QT_TEC_TOTAL AS TEC FROM TECNICO AS T INNER JOIN IES AS I ON T.CO_IES=I.CO_IES)TEC_M;

