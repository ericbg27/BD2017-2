CREATE TRIGGER Trig
BEFORE INSERT ON SEXO
FOR EACH ROW
	BEGIN
	IF (NEW.IN_SEXO != 0 AND NEW.IN_SEXO != 1) 
	THEN
  		SET NEW.IN_SEXO = NULL;
	END IF;
END $
DELIMITER $;
